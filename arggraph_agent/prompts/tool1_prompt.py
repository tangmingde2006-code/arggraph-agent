"""Tool 1 Prompt 模板：ADU Segmenter（ADU 切分器）

对应 SOP §1（ADU 划分规则）、§2（分析范围）
"""

TOOL1_PROMPT = """## 任务：ADU 切分

你需要将以下议论文文本切分为论证单元（ADU, Argumentative Discourse Unit）。
ADU 是"在论证中发挥单一角色的文本跨度"。

### 切分规则（严格按以下优先级执行）

#### 第一阶段：段落分解
将原文按自然段分解为 {P1, P2, ..., Pn}。

#### 第二阶段：一级切分（句号类标点）
对每个段落，以以下标点为界进行一级切分：
- 句号（。）
- 问号（？）
- 感叹号（！）
- 分号（；）
每个切分结果称为一个基础句。

#### 第三阶段：二级切分（关联词触发的精细切分）
对于一级切分后仍然较长的句子，如果包含以下关联词组合，进行二级切分：

| 关联词模式 | 切分方式 | 示例 |
|-----------|---------|------|
| 因为……所以…… | 切为两个单元 | "因为勤奋 / 所以成功" |
| 虽然……但是…… | 切为两个单元 | "虽然困难 / 但是值得" |
| 如果……那么…… | 切为两个单元 | "如果努力 / 那么有收获" |
| 不是……而是…… | 切为两个单元 | "不是运气 / 而是积累" |
| 不仅……而且…… | 切为两个单元 | "不仅是知识 / 而且是智慧" |
| 一方面……另一方面…… | 切为两个单元 | |
| 例如……这说明…… | 切为两个单元 | |
| 然而…… / 但是…… | 从前半部分切出 | |

#### 核心原则：默认不切分
- 当无法确定是否应该切分时，默认**不切分**
- 仅在有显式关联词触发时才进行二级切分
- 宁可少切，不可过切

### 编号规则
- 格式：Pn_Sm
- n = 自然段序号（从 1 开始）
- m = 该段内第 m 个 ADU（从 1 开始）

### 输出格式
必须严格按照以下 JSON 格式输出（不要输出 markdown code block 标记，只输出纯 JSON）：

{
  "segments": [
    {"id": "P1_S1", "text": "……"},
    {"id": "P1_S2", "text": "……"},
    {"id": "P2_S1", "text": "……"}
  ],
  "paragraph_count": 4,
  "total_adus": 15
}

### 注意事项
1. 每个 segment 的 text 必须是原文中的连续文本片段
2. 所有 segment 的 text 按顺序拼接应能还原原文
3. 如果进行了二级切分，在 text 中给出触发关联词作为理由（可在 reasoning 字段中说明，但不强制）
4. 严格按照 JSON 格式输出，不要添加任何额外的解释文字"""


def get_tool1_prompt() -> str:
    return TOOL1_PROMPT


def build_tool1_messages(essay_text: str) -> list:
    """构建 Tool 1 调用的 messages"""
    return [
        {"role": "system", "content": TOOL1_PROMPT},
        {"role": "user", "content": f"请对以下议论文进行 ADU 切分：\n\n{essay_text}"},
    ]
